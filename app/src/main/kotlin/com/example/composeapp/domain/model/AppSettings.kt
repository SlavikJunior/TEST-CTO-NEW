/*
 * Модель настроек приложения
 * 
 * Архитектурное обоснование:
 * - Инкапсулирует пользовательские настройки приложения
 * - Используется с DataStore для персистентного хранения (предпочтительнее SharedPreferences)
 * - Модель независима от механизма хранения (Clean Architecture)
 * 
 * Технологические решения:
 * - DataStore выбран как современная замена SharedPreferences с поддержкой Flow
 * - DataStore обеспечивает типобезопасность и асинхронный доступ через корутины
 * - nickname используется для идентификации устройства в сети (отображается другим пользователям)
 * - sharedFolderPath определяет каталог с файлами, доступными для обмена
 * - Flow позволяет UI реактивно реагировать на изменения настроек
 */
package com.example.composeapp.domain.model

/**
 * Настройки приложения
 * 
 * @property nickname Имя устройства, отображаемое другим пользователям в сети
 * @property sharedFolderPath Путь к папке с файлами, доступными для обмена
 */
data class AppSettings(
    val nickname: String,
    val sharedFolderPath: String
)
