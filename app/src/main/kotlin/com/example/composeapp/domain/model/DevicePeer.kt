/*
 * Модель устройства-пира в P2P сети
 * 
 * Архитектурное обоснование:
 * - Использование data class обеспечивает неизменяемость и автоматическую генерацию equals/hashCode
 * - Модель содержит минимально необходимые данные для идентификации и подключения к удаленному устройству
 * - mDNS (multicast DNS) выбран для обнаружения устройств в локальной сети без центрального сервера
 * - Поля ipAddress и port используются для установления TCP соединения после обнаружения через mDNS
 * 
 * Технологические решения:
 * - mDNS позволяет обнаруживать устройства автоматически без ручного ввода адресов
 * - Хранение deviceId обеспечивает уникальную идентификацию устройства между сессиями
 * - Модель полностью независима от Android фреймворка (Clean Architecture)
 */
package com.example.composeapp.domain.model

/**
 * Представляет удаленное устройство в P2P сети
 * 
 * @property deviceId Уникальный идентификатор устройства
 * @property nickname Отображаемое имя устройства, заданное пользователем
 * @property ipAddress IP адрес устройства в локальной сети для установления TCP соединения
 * @property port TCP порт, на котором устройство прослушивает входящие соединения
 * @property isOnline Статус доступности устройства (true если устройство активно и доступно)
 */
data class DevicePeer(
    val deviceId: String,
    val nickname: String,
    val ipAddress: String,
    val port: Int,
    val isOnline: Boolean
)
