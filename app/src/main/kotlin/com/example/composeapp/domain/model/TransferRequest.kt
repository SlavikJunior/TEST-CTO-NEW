/*
 * Модель запроса на передачу файла
 * 
 * Архитектурное обоснование:
 * - Инкапсулирует всю информацию, необходимую для инициации передачи файла
 * - Содержит идентификаторы как файла, так и устройства-источника
 * - Модель используется для передачи команд между слоями приложения (Clean Architecture)
 * 
 * Технологические решения:
 * - deviceId определяет устройство-источник для установления TCP соединения
 * - fileId идентифицирует запрашиваемый файл на удаленном устройстве
 * - destinationPath задает локальное расположение для сохранения файла
 * - Модель не зависит от деталей протокола передачи (JSON/TCP)
 */
package com.example.composeapp.domain.model

/**
 * Представляет запрос на скачивание файла с удаленного устройства
 * 
 * @property deviceId Идентификатор устройства-источника файла
 * @property fileId Идентификатор файла для скачивания
 * @property destinationPath Локальный путь для сохранения скачанного файла
 */
data class TransferRequest(
    val deviceId: String,
    val fileId: String,
    val destinationPath: String
)
