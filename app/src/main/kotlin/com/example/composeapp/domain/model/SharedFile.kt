/*
 * Модель файла, доступного для обмена через P2P
 * 
 * Архитектурное обоснование:
 * - Модель содержит метаданные файла, необходимые для отображения и передачи
 * - Независимость от деталей реализации файловой системы (Clean Architecture)
 * - Хранение размера файла позволяет рассчитывать прогресс передачи и проверять доступное место
 * 
 * Технологические решения:
 * - fileId используется для уникальной идентификации файла в запросах передачи
 * - mimeType позволяет корректно отображать иконки и определять тип файла
 * - relativePath сохраняет структуру каталогов при передаче файлов
 * - lastModified используется для определения актуальности файла
 */
package com.example.composeapp.domain.model

/**
 * Представляет файл, доступный для обмена
 * 
 * @property fileId Уникальный идентификатор файла в системе
 * @property name Имя файла с расширением
 * @property size Размер файла в байтах
 * @property mimeType MIME тип файла для определения категории (image/*, video/*, etc.)
 * @property relativePath Относительный путь файла от корня общей папки
 * @property lastModified Временная метка последнего изменения файла (Unix timestamp в миллисекундах)
 */
data class SharedFile(
    val fileId: String,
    val name: String,
    val size: Long,
    val mimeType: String,
    val relativePath: String,
    val lastModified: Long
)
